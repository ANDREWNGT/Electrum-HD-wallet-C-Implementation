std::string version::seed_prefix(std::string seed_type) {
    if (seed_type == "NULL") {
        seed_type = "segwit";
    }
    ///////////////////////////////////////////////////////////////////////////////
    if (seed_type == "standard") {
        return SEED_PREFIX;
    }
    else if (seed_type == "segwit") {
        return SEED_PREFIX_SW;
    }
    else if (seed_type == "2fa") {
        return SEED_PREFIX_2FA;
    }
    else if (seed_type == "2fa_segwit") {
        return SEED_PREFIX_2FA_SW;
    }
    else {
        throw ("unknown seed_type.");
    }
}

